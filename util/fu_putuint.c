#include "ft_printf.h"

int	fu_putuint(unsigned int n)
{
	if (n == 0)
		write (1, "0", 1);
	else
	{
		if (n / 10 != 0)
			fu_putuint(n / 10);
		ft_putchar_fd((n % 10) + '0', 1);
	}
	return (fu_numlen(n));
}
